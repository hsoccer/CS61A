def flatten(lst):
    """Returns a flattened version of lst.

    >>> flatten([1, 2, 3])     # normal list
    [1, 2, 3]
    >>> x = [1, [2, 3], 4]      # deep list
    >>> flatten(x)
    [1, 2, 3, 4]
    >>> x = [[1, [1, 1]], 1, [1, 1]] # deep list
    >>> flatten(x)
    [1, 1, 1, 1, 1, 1]
    """
    "*** YOUR CODE HERE ***"

    def memo(i, l):
        if i == len(lst):
            return l
        elif type(lst[i]) == list:
            return memo(i+1, l+flatten(lst[i]))
        else:
            return memo(i+1, l+[lst[i]])

    return memo(0, [])

def merge(lst1, lst2):
    """Merges two sorted lists.

    >>> merge([1, 3, 5], [2, 4, 6])
    [1, 2, 3, 4, 5, 6]
    >>> merge([], [2, 4, 6])
    [2, 4, 6]
    >>> merge([1, 2, 3], [])
    [1, 2, 3]
    >>> merge([5, 7], [2, 4, 6])
    [2, 4, 5, 6, 7]
    """
    "*** YOUR CODE HERE ***"

    lst = lst1 + lst2

    def func(l, i):
        if i == 0:
            return func(l, i+1)
        elif i == len(l):
            return l
        elif l[i] < l[i-1]:
            l[i], l[i-1] = l[i-1], l[i]
            return func(l, 0)
        else:
            return func(l, i+1)

    return func(lst, 0)


def mergesort(seq):
    """Mergesort algorithm.

    >>> mergesort([4, 2, 5, 2, 1])
    [1, 2, 2, 4, 5]
    >>> mergesort([])     # sorting an empty list
    []
    >>> mergesort([1])   # sorting a one-element list
    [1]
    """
    "*** YOUR CODE HERE ***"
    if seq == []:
        return []
    return merge(seq, [])
